#include <stdio.h>

/**
 * add - adds 2 integers
 * @a: first integer
 * @b: 2nd integer
 * Return: the result of two integers
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - func that subtracts 2 integers
 * @a: first integer
 * @b: 2nd integer
 * Return: the result of two integers
 */

int sub(int a, int b)
{
        return (a - b);
}

/**
 * div - divides 2 integers
 * @a: first integer
 * @b: 2nd integer
 * Return: the result of two integers
 */

int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - shows a remainder of 2 integers
 * @a: first integer
 * @b: 2nd integer
 * Return: the result of two integers
 */

int mod(int a, int b)
{
        return (a % b);
}

/**
 * mult - multiplies 2 integers
 * @a: first integer
 * @b: 2nd integer
 * Return: the result of two integers
 */

int mult(int a, int b)
{
        return (a * b);
}
